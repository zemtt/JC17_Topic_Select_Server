package com.jc17.select.dao;

public class Select {
    private String select_id;
    private String s_id;
    private String sub_id;
    private int selected;

    public String getSelect_id() { return select_id; }

    public void setSelect_id(String select_id) { this.select_id = select_id; }

    public String getS_id() { return s_id; }

    public void setS_id(String s_id) { this.s_id = s_id; }

    public String getSub_id() { return sub_id; }

    public void setSub_id(String sub_id) { this.sub_id = sub_id; }

    public int getSelected() { return selected; }

    public void setSelected(int selected) { this.selected = selected; }
}
